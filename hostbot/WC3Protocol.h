#ifndef _WC3PROTOCOL_H_
#define _WC3PROTOCOL_H_

enum Protocol {
	WC3_PING_FROM_HOST		= 1,	// 0x01
	WC3_SLOTINFOJOIN		= 4,	// 0x04
	WC3_REJECTJOIN			= 5,	// 0x05
	WC3_PLAYERINFO			= 6,	// 0x06
	WC3_PLAYERLEAVE_OTHERS	= 7,	// 0x07
	WC3_GAMELOADED_OTHERS	= 8,	// 0x08
	WC3_SLOTINFO			= 9,	// 0x09
	WC3_COUNTDOWN_START	= 10,	// 0x0A
	WC3_COUNTDOWN_END		= 11,	// 0x0B
	WC3_INCOMING_ACTION	= 12,	// 0x0C
	WC3_CHAT_FROM_HOST		= 15,	// 0x0F
	WC3_START_LAG			= 16,	// 0x10
	WC3_STOP_LAG			= 17,	// 0x11
	WC3_HOST_KICK_PLAYER	= 28,	// 0x1C
	WC3_REQJOIN			= 30,	// 0x1E
	WC3_LEAVEGAME			= 33,	// 0x21
	WC3_GAMELOADED_SELF	= 35,	// 0x23
	WC3_OUTGOING_ACTION	= 38,	// 0x26
	WC3_OUTGOING_KEEPALIVE	= 39,	// 0x27
	WC3_CHAT_TO_HOST		= 40,	// 0x28
	WC3_DROPREQ			= 41,	// 0x29
	WC3_SEARCHGAME			= 47,	// 0x2F (UDP/LAN)
	WC3_GAMEINFO			= 48,	// 0x30 (UDP/LAN)
	WC3_CREATEGAME			= 49,	// 0x31 (UDP/LAN)
	WC3_REFRESHGAME		= 50,	// 0x32 (UDP/LAN)
	WC3_DECREATEGAME		= 51,	// 0x33 (UDP/LAN)
	WC3_CHAT_OTHERS		= 52,	// 0x34
	WC3_PING_FROM_OTHERS	= 53,	// 0x35
	WC3_PONG_TO_OTHERS		= 54,	// 0x36
	WC3_MAPCHECK			= 61,	// 0x3D
	WC3_STARTDOWNLOAD		= 63,	// 0x3F
	WC3_MAPSIZE			= 66,	// 0x42
	WC3_MAPPART			= 67,	// 0x43
	WC3_MAPPARTOK			= 68,	// 0x44
	WC3_MAPPARTNOTOK		= 69,	// 0x45 - just a guess, received this packet after forgetting to send a crc in WC3_MAPPART (f7 45 0a 00 01 02 01 00 00 00)
	WC3_PONG_TO_HOST		= 70,	// 0x46
	WC3_INCOMING_ACTION2	= 72	// 0x48 - received this packet when there are too many actions to fit in WC3_INCOMING_ACTION
};

#endif // _WC3PROTOCOL_H_
